#!/usr/bin/env node
/**
 * Regenerate per-subject flashcard shards in src/data/flashcards/
 * Usage: node scripts/build-flashcards.mjs
 */
import { mkdirSync, writeFileSync, readFileSync, existsSync } from "fs";
import { join, dirname } from "path";
import { fileURLToPath } from "url";
import { calculus1 } from "../src/data/calculus1.js";
import { calculus2 } from "../src/data/calculus2.js";
import { physics1 } from "../src/data/physics1.js";
import { physics2 } from "../src/data/physics2.js";
import { chemistry } from "../src/data/chemistry.js";
import { programming } from "../src/data/programming.js";
import { english } from "../src/data/english.js";
import { repairFlashcardLatex } from "../src/utils/repairFlashcardLatex.js";

const __dirname = dirname(fileURLToPath(import.meta.url));
const SHARD_DIR = join(__dirname, "../src/data/flashcards");
const VOCAB_DIR = join(__dirname, "../src/data/vocab");

const CEFR_LEVELS = ["a1", "a2", "b1", "b2", "c1", "c2"];
const DOMAIN_IDS = ["science", "engineering", "political", "judicial"];

function loadEnglishVocabCards() {
  const cards = [];
  for (const level of CEFR_LEVELS) {
    const path = join(VOCAB_DIR, `cefr-${level}.json`);
    if (!existsSync(path)) {
      console.warn(`Missing vocab file: ${path}`);
      continue;
    }
    const chunk = JSON.parse(readFileSync(path, "utf8"));
    for (const card of chunk) {
      cards.push({ ...card, subjectId: "english" });
    }
  }
  for (const domain of DOMAIN_IDS) {
    const path = join(VOCAB_DIR, `domain-${domain}.json`);
    if (!existsSync(path)) {
      console.warn(`Missing domain file: ${path}`);
      continue;
    }
    const chunk = JSON.parse(readFileSync(path, "utf8"));
    for (const card of chunk) {
      cards.push({ ...card, subjectId: "english" });
    }
  }
  return cards;
}

const englishWithVocab = {
  ...english,
  flashcards: loadEnglishVocabCards(),
};

const SUBJECTS = [calculus1, calculus2, physics1, physics2, chemistry, programming, englishWithVocab];

function normalizeCard(card, subjectId) {
  const entry = {
    id: card.id,
    front: repairFlashcardLatex(card.front),
    back: repairFlashcardLatex(card.back),
    subjectId,
  };
  if (card.level) entry.level = card.level;
  if (card.pos) entry.pos = card.pos;
  if (card.translations?.length) entry.translations = card.translations;
  if (card.domain) entry.domain = card.domain;
  if (card.domainLabel) entry.domainLabel = card.domainLabel;
  return entry;
}

function buildBySubject() {
  const seen = new Set();
  const bySubject = Object.fromEntries(SUBJECTS.map((s) => [s.id, []]));

  for (const subject of SUBJECTS) {
    for (const card of subject.flashcards ?? []) {
      if (!card?.id || !card.front || !card.back) {
        console.warn(`Skip invalid card in ${subject.id}:`, card?.id ?? "(no id)");
        continue;
      }
      if (seen.has(card.id)) {
        console.warn(`Duplicate id skipped: ${card.id}`);
        continue;
      }
      seen.add(card.id);
      bySubject[subject.id].push(normalizeCard(card, subject.id));
    }
  }

  return bySubject;
}

mkdirSync(SHARD_DIR, { recursive: true });
const bySubject = buildBySubject();
const counts = {};

for (const [subjectId, cards] of Object.entries(bySubject)) {
  const path = join(SHARD_DIR, `${subjectId}.json`);
  writeFileSync(path, JSON.stringify(cards));
  counts[subjectId] = cards.length;
  console.log(`Wrote ${path} (${cards.length} cards)`);
}

const manifest = `/** Auto-generated by scripts/build-flashcards.mjs — do not edit manually */
export const FLASHCARD_COUNTS = ${JSON.stringify(counts, null, 2)};

export const FLASHCARD_SUBJECT_IDS = ${JSON.stringify(Object.keys(counts), null, 2)};
`;

writeFileSync(join(SHARD_DIR, "manifest.js"), manifest, "utf8");
console.log("Counts:", counts);
console.log("Total:", Object.values(counts).reduce((a, b) => a + b, 0));

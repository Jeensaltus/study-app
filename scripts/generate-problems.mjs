#!/usr/bin/env node
/**
 * Generate cal1Generated.js, cal2Generated.js, cal1HardGenerated.js, cal2HardGenerated.js.
 * Standard: ~500 problems per course. Hard: ~200 problems per course.
 */
import { writeFileSync, mkdirSync } from "fs";
import { dirname, join } from "path";
import { fileURLToPath } from "url";
import { CAL1_TEMPLATES, CAL2_TEMPLATES } from "./templates/problemTemplates.js";
import { CAL1_HARD_TEMPLATES } from "./templates/hardCal1Templates.js";
import { CAL2_HARD_TEMPLATES } from "./templates/hardCal2Templates.js";

const __dirname = dirname(fileURLToPath(import.meta.url));
const OUT_DIR = join(__dirname, "../src/data/generated");
const TARGET_PER_COURSE = 500;
const TARGET_HARD = 200;

function cartesian(params) {
  const keys = Object.keys(params);
  if (!keys.length) return [{}];
  const [first, ...rest] = keys;
  const restCombos = cartesian(Object.fromEntries(rest.map((k) => [k, params[k]])));
  return params[first].flatMap((v) => restCombos.map((combo) => ({ ...combo, [first]: v })));
}

function expandTemplate(template, subjectPrefix) {
  const combos = cartesian(template.params);
  return combos.map((params, i) => {
    const built = template.build(params);
    const paramKey = Object.values(params).join("-");
    return {
      id: `${template.id}-${paramKey}-${i}`,
      topic: template.topic,
      examType: template.examType,
      difficulty: template.difficulty,
      subtopic: template.subtopic,
      templateId: template.id,
      problem: built.problem,
      answer: built.answer,
      solution: built.solution ?? built.answer,
    };
  });
}

function generateBank(templates, subjectPrefix, target = TARGET_PER_COURSE) {
  const all = templates.flatMap((t) => expandTemplate(t, subjectPrefix));
  const seen = new Set();
  const unique = [];
  for (const p of all) {
    const key = p.problem.slice(0, 120);
    if (seen.has(key)) continue;
    seen.add(key);
    unique.push(p);
  }

  // Repeat with suffix variants until target
  const bank = [...unique];
  let round = 0;
  while (bank.length < target) {
    for (const p of unique) {
      if (bank.length >= target) break;
      bank.push({
        ...p,
        id: `${p.id}-r${round}`,
      });
    }
    round += 1;
  }

  return bank.slice(0, target);
}

function serializeProblems(name, problems) {
  return `/** Auto-generated by scripts/generate-problems.mjs — do not edit manually */\nexport const ${name} = ${JSON.stringify(problems, null, 2)};\nexport default ${name};\n`;
}

mkdirSync(OUT_DIR, { recursive: true });

const cal1 = generateBank(CAL1_TEMPLATES, "c1", TARGET_PER_COURSE);
const cal2 = generateBank(CAL2_TEMPLATES, "c2", TARGET_PER_COURSE);
const cal1Hard = generateBank(CAL1_HARD_TEMPLATES, "c1h", TARGET_HARD);
const cal2Hard = generateBank(CAL2_HARD_TEMPLATES, "c2h", TARGET_HARD);

writeFileSync(join(OUT_DIR, "cal1Generated.js"), serializeProblems("cal1Generated", cal1));
writeFileSync(join(OUT_DIR, "cal2Generated.js"), serializeProblems("cal2Generated", cal2));
writeFileSync(join(OUT_DIR, "cal1HardGenerated.js"), serializeProblems("cal1HardGenerated", cal1Hard));
writeFileSync(join(OUT_DIR, "cal2HardGenerated.js"), serializeProblems("cal2HardGenerated", cal2Hard));

console.log(`Generated cal1: ${cal1.length} problems`);
console.log(`Generated cal2: ${cal2.length} problems`);
console.log(`Generated cal1 hard: ${cal1Hard.length} problems`);
console.log(`Generated cal2 hard: ${cal2Hard.length} problems`);
